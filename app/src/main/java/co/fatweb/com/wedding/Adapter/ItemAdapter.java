package co.fatweb.com.wedding.Adapter;

public class ItemAdapter {
    private int image;
    private String text;
    private String id;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public int getImage() {
        return image;
    }
    public void setImage(int image) {
        this.image = image;
    }
    public void setText(String text) {
        this.text = text;
    }
    public String getText() {
        return text;
    }}